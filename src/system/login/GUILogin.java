    private void doLogin() {

        objlogin = new OBJLogin(
                txtuname.getText(),
                txtpass.getText());

        boolean status = SERLogin.login(objlogin);

        if (status) {
            accountpackage.AccountPackage.user = txtuname.getText();
            this.dispose();
            new mainApp.MainFrame().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid User name or Password", "Login Error!", JOptionPane.ERROR_MESSAGE);
            txtuname.grabFocus();
            txtuname.selectAll();
        }
    }

