    private boolean checkVerifications() {
        return chkVoucher.isSelected() && chkReceipt.isSelected() && chkCheque.isSelected() && chkInvoice.isSelected() && chkGRN.isSelected();
    }

    private void doDayEnd() {
        try {
            Connection conn = accountpackage.AccountPackage.connect;
            conn.setAutoCommit(false);
            DayEnd.paneltyCalculate(conn);
            DayEnd.calculateDate(conn);
            conn.commit();
        } catch (SQLException ex) {
            Logger.getLogger(DayEnd.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
